"""
Реализовать программу работы с органическими клетками. Необходимо создать класс Клетка.
В его конструкторе инициализировать параметр, соответствующий количеству клеток (целое число).
В классе должны быть реализованы методы перегрузки арифметических операторов: сложение (add()),
вычитание (sub()), умножение (mul()), деление (truediv()).Данные методы должны применяться
только к клеткам и выполнять увеличение, уменьшение, умножение и обычное (не целочисленное)
деление клеток, соответственно. В методе деления должно осуществляться округление значения до целого числа.
Сложение. Объединение двух клеток. При этом число ячеек общей клетки должно равняться сумме
ячеек исходных двух клеток. Вычитание. Участвуют две клетки. Операцию необходимо выполнять
только если разность количества ячеек двух клеток больше нуля, иначе выводить соответствующее сообщение.
Умножение. Создается общая клетка из двух. Число ячеек общей клетки определяется как произведение
количества ячеек этих двух клеток. Деление. Создается общая клетка из двух. Число ячеек общей
клетки определяется как целочисленное деление количества ячеек этих двух клеток.
В классе необходимо реализовать метод make_order(), принимающий экземпляр класса и
количество ячеек в ряду. Данный метод позволяет организовать ячейки по рядам.
Метод должен возвращать строку вида **\n\n***..., где количество ячеек между \n равно переданному аргументу.
Если ячеек на формирование ряда не хватает, то в последний ряд записываются все оставшиеся.
Например, количество ячеек клетки равняется 12, количество ячеек в ряду — 5. Тогда метод make_order() вернет строку: **\n\n.
Или, количество ячеек клетки равняется 15, количество ячеек в ряду — 5. Тогда метод make_order() вернет строку: **\n\n***.
Подсказка: подробный список операторов для перегрузки доступен по ссылке.
"""

class Cell:
    def __init__(self, count):
        try:
            count = int(count)
            self.count = count
        except ValueError:
            print("Значение клеток должно быть целочисленным")

    def __add__(self, other):
        return Cell(self.count + other.count)

    def __sub__(self, other):
        if self.count >= other.count:
            return Cell(self.count - other.count)
        else:
            print("Результат меньше 0, вычитание невозможно")

    def __mul__(self, other):
        return Cell(self.count * other.count)

    def __truediv__(self, other):
        if other.count != 0:
            return Cell(round(self.count / other.count))
        else:
            print("Деление на 0!")

    def make_order(self, row):
        s = self.count // row
        p = self.count % row
        rez = []
        for i in range(s):
            rez.append("*"*row)
        if p != 0:
            rez.append("*" * p)
        return "\n\n".join(rez)



c1 = Cell(25)
c2 = Cell(10)
c3 = Cell(0)
print((c1 + c2).count)
print((c1 - c2).count)
print(c2-c1)
print((c1*c2).count)
print(c1/c3)
print((c1/c2).count)
print(c1.make_order(3))
